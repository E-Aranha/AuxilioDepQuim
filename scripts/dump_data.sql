CREATE TABLE TERCEIROS(
    NUCPFCNPJ VARCHAR(14),
    NOME VARCHAR(30),
    TIPO VARCHAR(20),
    CONSTRAINT PK_TERCEIROS PRIMARY KEY(NUCPFCNPJ),  
    CONSTRAINT CK_TERCEIROS CHECK(
            (LENGTH(NUCPFCNPJ)= 11 AND TIPO = 'PESSOA FISICA') 
        OR  (LENGTH(NUCPFCNPJ) = 14 AND TIPO = 'EMPRESA PARCEIRA')
        OR  (LENGTH(NUCPFCNPJ) = 14 AND TIPO = 'ONG')
    )
    --Realizar o check pra tratar cpf e cnpj
);

